# -*- coding: utf-8 -*-
"""
مشروع Grades - تقدير درجة الطالب
الكاتب: [اسمك]
التاريخ: 2024
الوصف: برنامج يطلب درجة الطالب ويعرض التقدير المناسب مع إمكانية الخروج
"""

def get_grade_estimate(degree):
    """
    تحويل الدرجة العددية إلى تقدير نصي
    
    المعاملات:
        degree (float): درجة الطالب من 0 إلى 100
        
    المخرجات:
        str: التقدير المناسب
    """
    if degree >= 90:
        return "ممتاز (A)"
    elif degree >= 80:
        return "جيد جداً (B)"
    elif degree >= 70:
        return "جيد (C)"
    elif degree >= 60:
        return "مقبول (D)"
    else:
        return "راسب (F)"

def main():
    """
    الدالة الرئيسية للبرنامج - تحتوي على حلقة تكرار حتى يطلب المستخدم الخروج
    """
    print("=" * 50)
    print("برنامج تقدير درجات الطلاب")
    print("=" * 50)
    
    while True:
        # عرض التعليمات للمستخدم
        print("\nأدخل درجة الطالب (من 0 إلى 100) أو اكتب 'خروج' لإنهاء البرنامج:")
        user_input = input("> ").strip()
        
        # التحقق من رغبة المستخدم في الخروج (مع تجاهل حالة الأحرف)
        if user_input.lower() == "خروج":
            print("\nتم إنهاء البرنامج. وداعاً!")
            break
        
        # محاولة تحويل المدخل إلى رقم مع معالجة الأخطاء
        try:
            degree = float(user_input)
        except ValueError:
            # حال إدخال المستخدم لنص غير رقمي وغير كلمة "خروج"
            print("خطأ: يرجى إدخال رقم صحيح أو كلمة 'خروج' فقط.")
            continue
        
        # التحقق من أن الدرجة في النطاق المسموح
        if degree < 0 or degree > 100:
            print("خطأ: الدرجة يجب أن تكون بين 0 و 100. حاول مرة أخرى.")
            continue
        
        # حساب التقدير وعرضه
        estimate = get_grade_estimate(degree)
        print(f"الدرجة: {degree} - التقدير: {estimate}")

# ضمان أن الكود يعمل فقط عند تشغيل الملف مباشرة وليس عند استيراده
if __name__ == "__main__":
    main()
